package demo_4;

public class DemoLeftshiftRightshiftOperator {
public static void main(String[] args) {
	int a=8;//binary format 1000
	int b=a<<2;//binary format 100000 gain bits
	System.out.println(b);
	int c=a>>2;//binary format 10.00 lose bits
	System.out.println(c);
	int a1=25;
	int b1=a1>>2;
	System.out.println(b1);
	
}
}
