package demo_4;

public class DemoBitwiseOperator {

	public static void main(String[] args) {
		int a=25;//binary format 11001
		int b=15;//binary format 01111
		int c=a & b;//binary format 01001
		System.out.println(c);
		
		int d= a | b; //binary format 11111
		System.out.println(d);
		
		
	}
}
